# EVENTS

TERMINATION = 0xFF

TIMEOUT = 0xFF01
UNKNOWN = 0xFF02
SERIAL_EVENTS = [TIMEOUT, UNKNOWN]

INIT = 0x00
ACTION_COMPLETE = 0x05
ACK = 0x06
UNABLE_TO_EXECUTE = 0x11
NACK = 0x15

CAMERA_EVENTS = [INIT, ACTION_COMPLETE, ACK, UNABLE_TO_EXECUTE, NACK]

# COMMAND
