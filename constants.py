READINT = 0x01
ACTION = 0x02
READVDATA = 0x04

SHOT = [ACTION, 2]
LCD_MODE = [ACTION, 8]

FRAMES_COUNT = [READINT, 10]
FRAMES_CLEAR_LAST = [ACTION, 0]
FRAMES_LAST = [READINT, 11]
FRAMES_SELECT = [SETINT, 4]
FRAMES_GET = [READVDATA, 14]
FRAMES_GET_LENGTH = [READINT, 12]

# DATA
